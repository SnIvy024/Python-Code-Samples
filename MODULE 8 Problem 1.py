# Problem 1 - taking two inputs from the user and checks if they are equal. It then prints a message showing whether the values are equal or not.

def check_equality():
   
    # Taking two inputs from the user

    input1 = input("Enter the first value: ")
    input2 = input("Enter the second value: ")
    
    # Checking if the two inputs are equal

    if input1 == input2:
        print("The two values are equal.")
    else:
        print("The two values are not equal.")

check_equality()  

