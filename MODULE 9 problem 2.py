# Problem 2: Creating a List with Numbers 0 to 10
# Description: I use a while loop to create a list named L that contains the numbers 0 to 10.

# Start an empty list L is the name so L=

L = []

# begin the counter variable using counter 

counter = 0

# Start the while loop

while counter <= 10:
    # current value of counter to the list L

    L.append(counter)
    
    # add the counter by 1
    counter += 1

# Print the list L to see result 
print(L)
